package com.lifeshs.common.constants.common;

/**
 * 
 *  用户状态
 *  @author dengfeng  
 *  @DateTime 2016-6-2 上午10:08:36
 */
public enum UserStatus {

	/**
	 * 正常
	 */
	normal(0, "正常"),
	/**
	 * 停用
	 */
	disenable(1, "停用"),
	/**
	 * 注销
	 */
	logoff(2, "注销"),
	/**
	 * 离职
	 */
	leaveoff(4, "离职");
	
	private int _value;
    private String _name;

    private UserStatus(int value, String name)
    {
       _value = value;
        _name = name;
    }

    public int value()
    {
        return _value;
    }

    public String getName()
    {
        return _name;
     }
}
