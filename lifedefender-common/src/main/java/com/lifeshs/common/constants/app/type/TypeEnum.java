public enum TypeEnum {

    APP(1, "生命守护-应用端"),
    
    MAPP(2, "生命守护-管理端");
    
    /** 值 */
    private int value;
    /** 描述 */
    private String remark;
    
    private TypeEnum(int value, String remark) {
        this.value = value;
        this.remark = remark;
    }
    
    public int value() {
        return this.value;
    }
    
    public String remark() {
        return this.remark;
    }
}
