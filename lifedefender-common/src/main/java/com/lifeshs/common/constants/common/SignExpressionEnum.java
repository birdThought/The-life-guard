package com.lifeshs.common.constants.common;

/**
 *  符号表达式结果枚举类型
 *  @author yuhang.weng
 *  @version 1.0
 *  @DateTime 2017年6月24日 上午11:11:34
 */
public enum SignExpressionEnum {

    /** 等于 */
    EQUALS("equals"),
    /** 在区间内 */
    BETWEEN("between"),
    /** 小于 */
    UNDER("under"),
    /** 大于 */
    OVER("over"),
    /** 小于等于 */
    UNDER_INCLUDE("underInclude"),
    /** 大于等于 */
    OVER_INCLUDE("overInclude"),
    /** 不等于 */
    NOT_EQUALS("notEquals");
    
    /** 值 */
    private String value;
    
    public String getValue() {
        return this.value;
    }
    
    private SignExpressionEnum(String value) {
        this.value = value;
    }
}
