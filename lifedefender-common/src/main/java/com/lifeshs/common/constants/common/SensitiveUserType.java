package com.lifeshs.common.constants.common;

/**
 *  敏感操作用户类型
 *  @author yuhang.weng
 *  @version 1.0
 *  @DateTime 2017年4月7日 上午11:19:00
 */
public enum SensitiveUserType {

    /** 普通用户 */
    USER(1, "user"),
    
    /** 机构用户 */
    ORG_USER(2, "orgUser");

    private int value;

    private String remark;

    private SensitiveUserType(int value, String remark) {
        this.value = value;
        this.remark = remark;
    }

    public int getValue() {
        return value;
    }

    public String getRemark() {
        return remark;
    }
}
