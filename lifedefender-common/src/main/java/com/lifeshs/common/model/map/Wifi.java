public class Wifi {

    /**
     * WIFI结点（路由器）的MAC地址
     */
    private String mac;

    /**
     * 当前WIFI广播的信号强度，单位是DBM(注:DBM都是负数,定位准确与否的关键参数)
     */
    private int singal;

    public String getMac() {
        return mac;
    }

    public void setMac(String mac) {
        this.mac = mac;
    }

    public int getSingal() {
        return singal;
    }

    public void setSingal(int singalstrength) {
        this.singal = singalstrength;
    }
}
