package com.lifeshs.common.model;

import com.lifeshs.common.constants.common.SignExpressionEnum;

public class SignExpressionDTO {

    private double start;
    
    private double end;
    
    private SignExpressionEnum expression;

    public double getStart() {
        return start;
    }

    public void setStart(double start) {
        this.start = start;
    }

    public double getEnd() {
        return end;
    }

    public void setEnd(double end) {
        this.end = end;
    }

    public SignExpressionEnum getExpression() {
        return expression;
    }

    public void setExpression(SignExpressionEnum expression) {
        this.expression = expression;
    }

    @Override
    public String toString() {
        return "SignExpressionDTO [start=" + start + ", end=" + end + ", expression=" + expression + "]";
    }
}
