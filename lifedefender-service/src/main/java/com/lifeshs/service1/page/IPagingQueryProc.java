package com.lifeshs.service1.page;

import java.util.List;

/**
 * 分布查询时获得总记录数的接口
 * Created by dengfeng on 2017/7/8 0008.
 */
public interface IPagingQueryProc<T> {
    /**
     * 获取总记录数
     * @return
     */
    int queryTotal();

    /**
     * 获取页数据
     * @return
     */
    List<T> queryData(int startRow, int pageSize);
}
