public class EnergyAnalyzeDTO {
	
	/** 基础代谢*/
	Double basicMetabolism;
	
	/** 所需能量*/
	Double requiredEnergy;
	
	/** 基础代谢偏差*/
	Double basicMetabolismDeviation;
	
	/** 所需能量偏差*/
	Double requiredEnergyDeviation;
	
	/** 基础代谢建议*/
	String basicMetabolismSuggetion;
	
	/** 所需能量建议*/
	String requiredEnergySuggetion;
	
	/** 错误信息 */
	String errorMessage;
	
	public EnergyAnalyzeDTO() {
		super();
		
	}

	public EnergyAnalyzeDTO(Double basicMetabolism, Double requiredEnergy, Double basicMetabolismDeviation,
			Double requiredEnergyDeviation, String basicMetabolismSuggetion, String requiredEnergySuggetion, String errorMessage) {
		super();
		this.basicMetabolism = basicMetabolism;
		this.requiredEnergy = requiredEnergy;
		this.basicMetabolismDeviation = basicMetabolismDeviation;
		this.requiredEnergyDeviation = requiredEnergyDeviation;
		this.basicMetabolismSuggetion = basicMetabolismSuggetion;
		this.requiredEnergySuggetion = requiredEnergySuggetion;
		this.errorMessage = errorMessage;
	}
	
	@Override
	public String toString() {
		return "EnergyAnalyzeDTO [basicMetabolism=" + basicMetabolism + ", requiredEnergy=" + requiredEnergy
		        + ", basicMetabolismDeviation=" + basicMetabolismDeviation + ", requiredEnergyDeviation="
		        + requiredEnergyDeviation + ", basicMetabolismSuggetion=" + basicMetabolismSuggetion
		        + ", requiredEnergySuggetion=" + requiredEnergySuggetion + ", errorMessage=" + errorMessage + "]";
	}

	public Double getBasicMetabolism() {
		return basicMetabolism;
	}
	
	public void setBasicMetabolism(Double basicMetabolism) {
		this.basicMetabolism = basicMetabolism;
	}
	
	public Double getRequiredEnergy() {
		return requiredEnergy;
	}
	
	public void setRequiredEnergy(Double requiredEnergy) {
		this.requiredEnergy = requiredEnergy;
	}
	
	public Double getBasicMetabolismDeviation() {
		return basicMetabolismDeviation;
	}
	
	public void setBasicMetabolismDeviation(Double basicMetabolismDeviation) {
		this.basicMetabolismDeviation = basicMetabolismDeviation;
	}
	
	public Double getRequiredEnergyDeviation() {
		return requiredEnergyDeviation;
	}
	
	public void setRequiredEnergyDeviation(Double requiredEnergyDeviation) {
		this.requiredEnergyDeviation = requiredEnergyDeviation;
	}
	
	public String getBasicMetabolismSuggetion() {
		return basicMetabolismSuggetion;
	}
	
	public void setBasicMetabolismSuggetion(String basicMetabolismSuggetion) {
		this.basicMetabolismSuggetion = basicMetabolismSuggetion;
	}
	
	public String getRequiredEnergySuggetion() {
		return requiredEnergySuggetion;
	}
	
	public void setRequiredEnergySuggetion(String requiredEnergySuggetion) {
		this.requiredEnergySuggetion = requiredEnergySuggetion;
	}

	public String getErrorMessage() {
		return errorMessage;
	}

	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}
	
	
}
