package com.lifeshs.pojo.org.server;

public class RecommendServe {

	private Integer orgServeId;

	private String serveName;

	private String orgName;

	private Integer chargeMode;

	private Integer timePrice;

	private Integer monthPrice;

	private Integer yearPrice;

	private Integer countOfOrder;
	
	private int price;

	public Integer getOrgServeId() {
		return orgServeId;
	}

	public void setOrgServeId(Integer orgServeId) {
		this.orgServeId = orgServeId;
	}

	public String getServeName() {
		return serveName;
	}

	public void setServeName(String serveName) {
		this.serveName = serveName;
	}

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public Integer getChargeMode() {
		return chargeMode;
	}

	public void setChargeMode(Integer chargeMode) {
		this.chargeMode = chargeMode;
	}

	public Integer getTimePrice() {
		return timePrice;
	}

	public void setTimePrice(Integer timePrice) {
		this.timePrice = timePrice;
	}

	public Integer getMonthPrice() {
		return monthPrice;
	}

	public void setMonthPrice(Integer monthPrice) {
		this.monthPrice = monthPrice;
	}

	public Integer getYearPrice() {
		return yearPrice;
	}

	public void setYearPrice(Integer yearPrice) {
		this.yearPrice = yearPrice;
	}

	public Integer getCountOfOrder() {
		return countOfOrder;
	}

	public void setCountOfOrder(Integer countOfOrder) {
		this.countOfOrder = countOfOrder;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

}
