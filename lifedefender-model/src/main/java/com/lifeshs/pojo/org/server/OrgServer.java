package com.lifeshs.pojo.org.server;

import java.util.Date;

/**
 * 服务管理
 * 
 * @author zhansi.Xu
 * @DateTime 2016年9月8日
 * @Comment
 */
public class OrgServer {
    private Integer id;// 序号
    private Integer orgServeId;// 机构服务id
    private String serviceName;// 服务名称
    private Date createDate;// 开通时间
    private int memberCount;// 会员数
    /** 0_免费，1_收费 */
    private Boolean hasFree;

    /** 免费时长（月），0无限制 */
    private Integer freeDate;

    /** 有按次收费：1_是，0_否 */
    private Boolean hasTime;

    /** 按次的价格 */
    private Integer timePrice;

    /** 有按月收费：1_是，0_否 */
    private Boolean hasMonth;

    /** 按月的价格 */
    private Integer monthPrice;

    /** 有按年收费：1_是，0_否 */
    private Boolean hasYear;

    /** 按年的价格 */
    private Integer yearPrice;

    private String classify;

    private String image;

    private Integer groupCount;

    private String code;

    public Boolean getHasFree() {
        return hasFree;
    }

    public void setHasFree(Boolean hasFree) {
        this.hasFree = hasFree;
    }

    public Integer getFreeDate() {
        return freeDate;
    }

    public void setFreeDate(Integer freeDate) {
        this.freeDate = freeDate;
    }

    public Boolean getHasTime() {
        return hasTime;
    }

    public void setHasTime(Boolean hasTime) {
        this.hasTime = hasTime;
    }

    public Integer getTimePrice() {
        return timePrice;
    }

    public void setTimePrice(Integer timePrice) {
        this.timePrice = timePrice;
    }

    public Boolean getHasMonth() {
        return hasMonth;
    }

    public void setHasMonth(Boolean hasMonth) {
        this.hasMonth = hasMonth;
    }

    public Integer getMonthPrice() {
        return monthPrice;
    }

    public void setMonthPrice(Integer monthPrice) {
        this.monthPrice = monthPrice;
    }

    public Boolean getHasYear() {
        return hasYear;
    }

    public void setHasYear(Boolean hasYear) {
        this.hasYear = hasYear;
    }

    public Integer getYearPrice() {
        return yearPrice;
    }

    public void setYearPrice(Integer yearPrice) {
        this.yearPrice = yearPrice;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getServiceName() {
        return serviceName;
    }

    public void setServiceName(String serviceName) {
        this.serviceName = serviceName;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public Integer getOrgServeId() {
        return orgServeId;
    }

    public void setOrgServeId(Integer orgServeId) {
        this.orgServeId = orgServeId;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

    public int getMemberCount() {
        return memberCount;
    }

    public void setMemberCount(int memberCount) {
        this.memberCount = memberCount;
    }

    public String getClassify() {
        return classify;
    }

    public void setClassify(String classify) {
        this.classify = classify;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public Integer getGroupCount() {
        return groupCount;
    }

    public void setGroupCount(Integer groupCount) {
        this.groupCount = groupCount;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    @Override
    public String toString() {
        return "OrgServer [id=" + id + ", orgServeId=" + orgServeId + ", serviceName=" + serviceName + ", createDate="
                + createDate + ", memberCount=" + memberCount + ", hasFree=" + hasFree + ", freeDate=" + freeDate
                + ", hasTime=" + hasTime + ", timePrice=" + timePrice + ", hasMonth=" + hasMonth + ", monthPrice="
                + monthPrice + ", hasYear=" + hasYear + ", yearPrice=" + yearPrice + ", classify=" + classify
                + ", image=" + image + ", groupCount=" + groupCount + ", code=" + code + "]";
    }

}
