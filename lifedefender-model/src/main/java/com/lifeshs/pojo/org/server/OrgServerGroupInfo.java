package com.lifeshs.pojo.org.server;

import java.util.Date;

/**
 * 群组信息
 * @author zhansi.Xu
 * @DateTime 2016年9月8日
 * @Comment
 */
public class OrgServerGroupInfo extends OrgServerGroupBase{
	private Date createDate;//创建时间
	private String admin;//管理员
	private int adminId;//管理员的
	private String server;//服务师
	public int getAdminId() {
		return adminId;
	}
	public void setAdminId(int adminId) {
		this.adminId = adminId;
	}
	public int getServerId() {
		return serverId;
	}
	public void setServerId(int serverId) {
		this.serverId = serverId;
	}
	private int serverId;//服务师的id
	private String qrCode;//群组二维码
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	public String getAdmin() {
		return admin;
	}
	public void setAdmin(String admin) {
		this.admin = admin;
	}
	public String getServer() {
		return server;
	}
	public void setServer(String server) {
		this.server = server;
	}
	public String getQrCode() {
		return qrCode;
	}
	public void setQrCode(String qrCode) {
		this.qrCode = qrCode;
	}
	@Override
	public String toString() {
		return "OrgServerGroupInfo [createDate=" + createDate + ", admin="
				+ admin + ", adminId=" + adminId + ", server=" + server
				+ ", serverId=" + serverId + ", qrCode=" + qrCode + "]";
	}
}
