package com.lifeshs.pojo.app.member;

/**
 * APP发送的字符串转换对象
 * 
 * @author yuhang.weng
 * @DateTime 2017年2月21日 下午2:45:25
 */
public class AppJSON {

    /** 设备类型 APP_I APP_A */
    private String type;

    private String ver;

    /** data对象 */
    private AppJSONDataDTO data;

    /** 切面新增数据 */
    private AppAopDTO aopData;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getVer() {
        return ver;
    }

    public void setVer(String ver) {
        this.ver = ver;
    }

    public AppJSONDataDTO getData() {
        return data;
    }

    public void setData(AppJSONDataDTO data) {
        this.data = data;
    }

    public AppAopDTO getAopData() {
        return aopData;
    }

    public void setAopData(AppAopDTO aopData) {
        this.aopData = aopData;
    }

    @Override
    public String toString() {
        return "AppJSON [type=" + type + ", ver=" + ver + ", data=" + data + ", aopData=" + aopData + "]";
    }

}
