package com.lifeshs.pojo.healthDevice;

import com.lifeshs.common.constants.common.HealthRank;

/**
 * 尿酸
 * 
 * @author yuhang.weng
 * @version 1.0
 * @DateTime 2017年4月24日 上午10:05:36
 */
public class UaDTO {

    private Integer id;

    /** 用户ID */
    private Integer userId;

    /** 状态：正常_0,异常_项目和 */
    private Long status;

    /** 尿酸 */
    private Float UA;

    /** 尿酸状态描述 */
    private String UAStatusDescription;

    private HealthRank UARank;

    private Boolean UAStatus;

    /** 尿酸正常范围 */
    private String UAArea;

    /** 测量日期 */
    private java.util.Date measureDate;

    /** 终端类型 */
    private String deviceType;

    /** 数据类型，1代表实时数据，2代表定时数据，3代表手动数据,4代表睡眠数据 */
    private Integer dataType;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Long getStatus() {
        return status;
    }

    public void setStatus(Long status) {
        this.status = status;
    }

    public Float getUA() {
        return UA;
    }

    public void setUA(Float UA) {
        this.UA = UA;
    }

    public String getUAStatusDescription() {
        return UAStatusDescription;
    }

    public void setUAStatusDescription(String UAStatusDescription) {
        this.UAStatusDescription = UAStatusDescription;
    }

    public HealthRank getUARank() {
        return UARank;
    }

    public void setUARank(HealthRank UARank) {
        this.UARank = UARank;
    }

    public String getUAArea() {
        return UAArea;
    }

    public void setUAArea(String UAArea) {
        this.UAArea = UAArea;
    }

    public java.util.Date getMeasureDate() {
        return measureDate;
    }

    public void setMeasureDate(java.util.Date measureDate) {
        this.measureDate = measureDate;
    }

    public String getDeviceType() {
        return deviceType;
    }

    public void setDeviceType(String deviceType) {
        this.deviceType = deviceType;
    }

    public Integer getDataType() {
        return dataType;
    }

    public void setDataType(Integer dataType) {
        this.dataType = dataType;
    }

    public Boolean getUAStatus() {
        return UAStatus;
    }

    public void setUAStatus(Boolean uAStatus) {
        UAStatus = uAStatus;
    }

    @Override
    public String toString() {
        return "UaDTO [id=" + id + ", userId=" + userId + ", status=" + status + ", UA=" + UA + ", UAStatusDescription="
                + UAStatusDescription + ", UARank=" + UARank + ", UAStatus=" + UAStatus + ", UAArea=" + UAArea
                + ", measureDate=" + measureDate + ", deviceType=" + deviceType + ", dataType=" + dataType + "]";
    }

}
