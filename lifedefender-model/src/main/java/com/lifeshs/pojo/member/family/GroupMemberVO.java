package com.lifeshs.pojo.member.family;

import java.util.Date;

/**
 * TUser的群组会员VO类
 * 
 * @author yuhang.weng
 * @DateTime 2016年9月22日 上午11:02:40
 */
public class GroupMemberVO {

    private Integer id;

    private String realName;

    private Integer age;

    private String mobile;

    private Boolean isCurrentUser;

    private Date birthday;

    private String photo;

    /** 身高 */
    private Float height;

    /** 体重 */
    private Float weight;

    /** 腰围 */
    private Float waist;

    /** 胸围 */
    private Float bust;

    /** 臀围 */
    private Float hip;

    /** 性别 */
    private Boolean sex;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getRealName() {
        return realName;
    }

    public void setRealName(String realName) {
        this.realName = realName;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public Boolean getIsCurrentUser() {
        return isCurrentUser;
    }

    public void setIsCurrentUser(Boolean isCurrentUser) {
        this.isCurrentUser = isCurrentUser;
    }

    public Date getBirthday() {
        return birthday;
    }

    public void setBirthday(Date birthday) {
        this.birthday = birthday;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public Float getHeight() {
        return height;
    }

    public void setHeight(Float height) {
        this.height = height;
    }

    public Float getWeight() {
        return weight;
    }

    public void setWeight(Float weight) {
        this.weight = weight;
    }

    public Float getWaist() {
        return waist;
    }

    public void setWaist(Float waist) {
        this.waist = waist;
    }

    public Float getBust() {
        return bust;
    }

    public void setBust(Float bust) {
        this.bust = bust;
    }

    public Float getHip() {
        return hip;
    }

    public void setHip(Float hip) {
        this.hip = hip;
    }

    public Boolean getSex() {
        return sex;
    }

    public void setSex(Boolean sex) {
        this.sex = sex;
    }

    @Override
    public String toString() {
        return "GroupMemberVO [id=" + id + ", realName=" + realName + ", age=" + age + ", mobile=" + mobile
                + ", isCurrentUser=" + isCurrentUser + ", birthday=" + birthday + ", photo=" + photo + ", height="
                + height + ", weight=" + weight + ", waist=" + waist + ", bust=" + bust + ", hip=" + hip + ", sex="
                + sex + "]";
    }

}
