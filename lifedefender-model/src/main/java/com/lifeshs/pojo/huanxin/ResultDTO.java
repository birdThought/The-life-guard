package com.lifeshs.pojo.huanxin;

import com.alibaba.fastjson.JSONArray;

/**
 * 环信返回信息对象
 * 
 * @author yuhang.weng
 * @version 1.0
 * @DateTime 2017年2月27日 上午9:37:30
 */
public class ResultDTO {

    /** 请求方法 */
    private String action;
    /** 应用 */
    private String application;
    /** 请求uri */
    private String uri;
    /** 实体集合 */
    private JSONArray entities;
    /** 返回数据 */
    private Object data;
    /** 时间戳 */
    private String timestamp;
    /**  */
    private Integer duration;
    /** */
    private String organization;
    /** 应用名称 */
    private String applicationName;

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getApplication() {
        return application;
    }

    public void setApplication(String application) {
        this.application = application;
    }

    public String getUri() {
        return uri;
    }

    public void setUri(String uri) {
        this.uri = uri;
    }

    public JSONArray getEntities() {
        return entities;
    }

    public void setEntities(JSONArray entities) {
        this.entities = entities;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public Integer getDuration() {
        return duration;
    }

    public void setDuration(Integer duration) {
        this.duration = duration;
    }

    public String getOrganization() {
        return organization;
    }

    public void setOrganization(String organization) {
        this.organization = organization;
    }

    public String getApplicationName() {
        return applicationName;
    }

    public void setApplicationName(String applicationName) {
        this.applicationName = applicationName;
    }

    @Override
    public String toString() {
        return "ResultDTO [action=" + action + ", application=" + application + ", uri=" + uri + ", entities="
                + entities + ", data=" + data + ", timestamp=" + timestamp + ", duration=" + duration
                + ", organization=" + organization + ", applicationName=" + applicationName + "]";
    }

}
