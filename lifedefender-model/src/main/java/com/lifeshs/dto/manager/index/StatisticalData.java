package com.lifeshs.dto.manager.index;

/**
 * 门店/服务师的业务数据统计
 * Created by dengfeng on 2017/6/22 0022.
 */
public class StatisticalData {

    //会员数量
    private int memberNumber;
    //员工数量
    private int orgUserNumber;
    //待办任务数量
    private int outstandingOrderNumber;
    //今日订单数量
    private int todayOrderNumber;
    //系统未读消息数据
    private int nonReadMessage;

    public int getMemberNumber() {
        return memberNumber;
    }

    public void setMemberNumber(int memberNumber) {
        this.memberNumber = memberNumber;
    }

    public int getOrgUserNumber() {
        return orgUserNumber;
    }

    public void setOrgUserNumber(int orgUserNumber) {
        this.orgUserNumber = orgUserNumber;
    }

    public int getOutstandingOrderNumber() {
        return outstandingOrderNumber;
    }

    public void setOutstandingOrderNumber(int outstandingOrderNumber) {
        this.outstandingOrderNumber = outstandingOrderNumber;
    }

    public int getTodayOrderNumber() {
        return todayOrderNumber;
    }

    public void setTodayOrderNumber(int todayOrderNumber) {
        this.todayOrderNumber = todayOrderNumber;
    }

    public int getNonReadMessage() {
        return nonReadMessage;
    }

    public void setNonReadMessage(int nonReadMessage) {
        this.nonReadMessage = nonReadMessage;
    }
}
