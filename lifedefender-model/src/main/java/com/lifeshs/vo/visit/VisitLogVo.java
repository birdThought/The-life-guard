package com.lifeshs.vo.visit;

import com.lifeshs.po.OrgPO;
import com.lifeshs.pojo.log.LoginLogDTO;

/**
 * 访问记录
 * 
 * @author shiqiang.zeng
 *
 */
public class VisitLogVo extends LoginLogDTO {

	private Integer id;
	/**机构*/
	private OrgPO orgPo;
	/** 会员登录次数 */
	private Integer memberCount;
	/** 机构员工登录次数 */
	private Integer orderCount;

	public Integer getMemberCount() {
		return memberCount;
	}

	public void setMemberCount(Integer memberCount) {
		this.memberCount = memberCount;
	}

	public Integer getOrderCount() {
		return orderCount;
	}

	public void setOrderCount(Integer orderCount) {
		this.orderCount = orderCount;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public OrgPO getOrgPo() {
		return orgPo;
	}

	public void setOrgPo(OrgPO orgPo) {
		this.orgPo = orgPo;
	}

}
