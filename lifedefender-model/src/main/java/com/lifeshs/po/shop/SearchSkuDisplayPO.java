package com.lifeshs.po.shop;

import java.util.List;

import com.lifeshs.shop.PictureDTO;
import com.lifeshs.shop.SkuDTO;

public class SearchSkuDisplayPO extends SkuDTO{
	
	private String goodName;
	private List<PictureDTO> pictureUrls;
	private String attrName;
	private String attrValue;
	
	
	
	public String getGoodName() {
		return goodName;
	}
	public void setGoodName(String goodName) {
		this.goodName = goodName;
	}
	
	
	public List<PictureDTO> getPictureUrls() {
		return pictureUrls;
	}
	public void setPictureUrls(List<PictureDTO> pictureUrls) {
		this.pictureUrls = pictureUrls;
	}
	public String getAttrName() {
		return attrName;
	}
	public void setAttrName(String attrName) {
		this.attrName = attrName;
	}
	public String getAttrValue() {
		return attrValue;
	}
	public void setAttrValue(String attrValue) {
		this.attrValue = attrValue;
	}
	
	
	
}
