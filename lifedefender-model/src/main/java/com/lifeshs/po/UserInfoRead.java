package com.lifeshs.po;

import java.util.Date;

public class UserInfoRead {
    private Integer id;

    private Integer userId;

    private Integer orgUserId;

    private Integer device;

    private Integer hasWarning;

    private Boolean isRead;

    private Date measureDate;

    private Date updateTime;

    private Date createTime;

    public UserInfoRead(Integer id, Integer userId, Integer orgUserId, Integer device, Integer hasWarning, Boolean isRead, Date measureDate, Date updateTime, Date createTime) {
        this.id = id;
        this.userId = userId;
        this.orgUserId = orgUserId;
        this.device = device;
        this.hasWarning = hasWarning;
        this.isRead = isRead;
        this.measureDate = measureDate;
        this.updateTime = updateTime;
        this.createTime = createTime;
    }

    public UserInfoRead() {
        super();
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getOrgUserId() {
        return orgUserId;
    }

    public void setOrgUserId(Integer orgUserId) {
        this.orgUserId = orgUserId;
    }

    public Integer getDevice() {
        return device;
    }

    public void setDevice(Integer device) {
        this.device = device;
    }

    public Integer getHasWarning() {
        return hasWarning;
    }

    public void setHasWarning(Integer hasWarning) {
        this.hasWarning = hasWarning;
    }

    public Boolean getIsRead() {
        return isRead;
    }

    public void setIsRead(Boolean isRead) {
        this.isRead = isRead;
    }

    public Date getMeasureDate() {
        return measureDate;
    }

    public void setMeasureDate(Date measureDate) {
        this.measureDate = measureDate;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}