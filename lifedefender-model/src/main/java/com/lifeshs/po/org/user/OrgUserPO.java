package com.lifeshs.po.org.user;

import lombok.Data;

/**
 *  机构用户PO
 *  @author yuhang.weng
 *  @version 1.0
 *  @DateTime 2018年1月19日 下午3:54:38
 */
@Data
public class OrgUserPO {

    private Integer id;

    /** 用户编号 */
    private String userCode;
    
    /** 用户编号 */
    private String userNo;
    
    /** 用户引荐人 */
    private String parentId;
    
    /** 用户登录名 */
    private String userName;

    /** 登录密码 */
    private String password;

    /** app token(也可用于web自动登录) */
    private String token;

    /** 真实姓名 */
    private String realName;

    /** 手机号码 */
    private String mobile;

    /** 0:未验证,1:已验证 */
    private Boolean mobileVerified;

    /** 用户邮箱 */
    private String email;

    /** 0:未验证,1:已验证 */
    private Boolean emailVerified;

    /** 性别0表示女,1表示男 */
    private Boolean sex;

    /** 出生日期 */
    private java.util.Date birthday;

    /** 工作电话 */
    private String tel;

    /** 家庭地址 */
    private String address;

    /** 微信openid */
    private String openId;

    /** 状态：正常_0,停用_1,注销_2,离职_4 */
    private Integer status;

    /** 用户类型:管理员_0,服务师_1,都有_2 */
    private Integer userType;

    /** 权限集合(可勾选32项) */
    private Integer permissionSet;

    /** 机构ID */
    private Integer orgId;

    /** 个人照片路径 */
    private String photo;

    /** 个人简介 */
    private String about;

    /** 个人详细介绍(html) */
    private String detail;

    /** 创建日期 */
    private java.util.Date createDate;

    /** 修改时间 */
    private java.util.Date modifyDate;

    /** 身份证 */
    private String idCard;

    /** 身份证件照路径1 */
    private String idCardPicOne;

    /** 身份证件照路径2 */
    private String idCardPicTwo;

    /** 专业特长 */
    private String expertise;

    /**
     * (职业名称：医生、护士、健康管理师、膳食健康师、公共健康咨询师 理疗师、中医师
     * 健身教练、健康管理师、膳食健康师、慢病诊疗专家（中医、西医、养生师） 瑜伽教练、药剂师、全科医生保姆 特级看护、营养师)
     */
    private String professionalName;

    /** 职业资格证照路径 */
    private String professionalPic;
}
